package com.fenfen.base;

public class Demo2 {
    public static void main(String[] args) {
        String a = "hello";
        int num = 9;
        System.out.println(a);
        System.out.println(num);


        //八大基本数据类型

        //整数,看大小可以去大写的类里面翻：比如Integer，Byte，ctrl+点击

        byte num2 = 7;//一个字节
        short num4 = 22;//两个字节
        int num1 =8;//四个字节
        long num3 = 31L;//long后面要跟一个大写的L   //8个字节



        //小数：浮点数
        float num5 = 51.5F;//尾巴也要加一个大写的F   //4个字节
        double num6 = 3.141592653589793238462643;//8个字节

        //字符类型：
        char name = 'A';//字符，只能整一个字符
        String namea = "fenfen";//字符串，注意！String不是关键字，是个类

        //布尔值
        boolean flag = true;
        boolean flag1 = false;//一个字节

    }
}
